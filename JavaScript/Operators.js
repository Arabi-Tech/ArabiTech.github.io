// =====================
// العمليات الحسابية في JavaScript
// =====================

// الزائد
let x = 5 + 7;

// الناقص
x = 5 - 7;

// الضرب
x = 5 * 7;

// القسمة (عادية)
x = 5 / 7;

// باقي القسمة
x = 5 % 7;

// القسمة بالتقريب للأسفل
x = Math.floor(5 / 7);

// الأس (القوة)
x = 5 ** 2; // 5² = 25


// =====================
// العمليات المقارنة
// =====================
let a = 5;
let b = 7;

x = a == b;   // يساوي؟ false
x = a != b;   // لا يساوي؟ true
x = a > b;    // أكبر من؟ false
x = a < b;    // أصغر من؟ true
x = a >= b;   // أكبر أو يساوي؟ false
x = a <= b;   // أصغر أو يساوي؟ true

// ملاحظة: استخدم "===" و "!==" للمقارنة الصارمة (تتحقق من القيمة والنوع)
x = a === b;  // false
x = a !== b;  // true


// =====================
// العمليات المنطقية
// =====================
x = true && false;  // AND -> false
x = true || false;  // OR  -> true
x = !true;          // NOT -> false


// =====================
// العمليات على النصوص والقوائم (المصفوفات)
// =====================

// جمع النصوص
let text = "Hello" + " " + "World";  // "Hello World"

// تكرار النصوص
text = "Hi! ".repeat(3);  // "Hi! Hi! Hi! "


// جمع القوائم (المصفوفات)
let list1 = [1, 2];
let list2 = [3, 4];
let combined = list1.concat(list2);  // [1, 2, 3, 4]

// تكرار القوائم
let listRepeated = list1.concat(list1); // [1, 2, 1, 2]

// أو بطريقة حديثة باستخدام spread operator
let listRepeated2 = [...list1, ...list1]; // [1, 2, 1, 2]
