# =====================
# Variables in Python
# =====================

# ======= النصوص =======
name = "Osama"
greeting = 'Hello, World!'

# ======= الأرقام =======
# أعداد صحيحة
num_int = 10
num_negative = -5

# أعداد عشرية
num_float = 3.14
num_scientific = 1.5e3  # 1500.0

# أعداد مركبة
num_complex = 2 + 3j
another_complex = complex(5, -1)

# ======= القيم المنطقية =======
is_student = True
is_teacher = False

# ======= القوائم (List) =======
fruits = ["apple", "banana", "orange"]
numbers = [1, 2, 3, 4, 5]

# ======= الطوابل (Tuple) =======
point = (10, 20)
person = ("Osama", 25, "Saudi")

# ======= المجموعات (Set) =======
colors = {"red", "green", "blue"}
unique_numbers = {1, 2, 3, 4, 5}

# ======= المجموعات الثابتة (FrozenSet) =======
frozen_colors = frozenset(["red", "green", "blue"])

# ======= القواميس (Dictionary) =======
student = {"name": "Osama", "age": 20, "grade": "A"}
settings = {"theme": "dark", "language": "ar"}

# ======= المدى / تسلسل الأرقام (Range) =======
nums = range(1, 6)  # 1, 2, 3, 4, 5

# ======= البيانات الثنائية (Binary Types) =======
data_bytes = b"hello"
data_bytearray = bytearray(b"hello")
data_memoryview = memoryview(b"hello")

# ======= النوع الخاص لا شيء (NoneType) =======
result = None
