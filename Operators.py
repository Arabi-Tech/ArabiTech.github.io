# العمليات الحسابية
# الزائد
x = 5 + 7

# الناقص
x = 5 - 7

# الضرب
x = 5 * 7

# القسمة
x = 5 / 7

# باقي القسمة
x = 5 % 7

# القسمة بالتقريب للأسفل
x = 5 // 7

# الأس (القوة)
x = 5 ** 2  # 5² = 25

# ======= العمليات المقارنة =======
a = 5
b = 7

x = a == b   # يساوي؟ False
x = a != b   # لا يساوي؟ True
x = a > b    # أكبر من؟ False
x = a < b    # أصغر من؟ True
x = a >= b   # أكبر أو يساوي؟ False
x = a <= b   # أصغر أو يساوي؟ True

# ======= العمليات المنطقية =======
x = True and False   # AND -> False
x = True or False    # OR  -> True
x = not True         # NOT -> False

# ======= العمليات على النصوص والقوائم =======
# جمع النصوص
text = "Hello" + " " + "World"  # "Hello World"

# تكرار النصوص
text = "Hi! " * 3  # "Hi! Hi! Hi! "

# جمع القوائم
list1 = [1, 2]
list2 = [3, 4]
combined = list1 + list2  # [1,2,3,4]

# تكرار القوائم
list_repeated = list1 * 2  # [1,2,1,2]
