#include <iostream>
using namespace std;

// =====================
// Functions in C++
// =====================

// ======= دالة بدون قيمة إرجاع وبدون معطيات =======
void greet() {
    cout << "مرحباً بك في برنامج C++!" << endl;
}

// ======= دالة بدون قيمة إرجاع مع معطيات =======
void printSum(int a, int b) {
    cout << "مجموع " << a << " + " << b << " = " << a + b << endl;
}

// ======= دالة مع قيمة إرجاع =======
int multiply(int a, int b) {
    return a * b;
}

// ======= دالة مع قيمة إرجاع ومع معطيات =======
double divide(double a, double b) {
    if(b != 0) {
        return a / b;
    } else {
        cout << "خطأ: القسمة على صفر!" << endl;
        return 0;
    }
}

int main() {
    // ======= استدعاء الدوال =======
    greet();  // دالة بدون معطيات وقيمة إرجاع

    printSum(5, 10);  // دالة مع معطيات بدون قيمة إرجاع

    int result = multiply(4, 7);  // دالة مع قيمة إرجاع
    cout << "الضرب: 4 * 7 = " << result << endl;

    double division = divide(20, 4);  // دالة مع معطيات وقيمة إرجاع
    cout << "القسمة: 20 / 4 = " << division << endl;

    division = divide(10, 0); // اختبار القسمة على صفر

    // ======= أفضل الممارسات =======
    // - اعطي اسم واضح لكل دالة يصف وظيفتها
    // - إذا كانت الدالة قصيرة وواضحة يمكن دمجها في ملف واحد
    // - استخدم التعليقات لشرح معطيات الدالة وقيمة الإرجاع
    // - قسّم البرنامج لدوال صغيرة لتسهيل الصيانة وإعادة الاستخدام

    return 0;
}
