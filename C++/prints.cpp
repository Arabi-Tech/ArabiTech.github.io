#include <iostream>
using namespace std;

int main() {
    // =====================
    // Printing and I/O in C++
    // =====================

    // ======= 1. الطباعة الأساسية =======
    cout << "Hello World" << endl; // endl لإضافة سطر جديد بعد الطباعة
    cout << "مرحبا بك في C++" << endl;

    // ======= 2. الطباعة مع متغيرات =======
    int age = 25;
    string name = "أسامة";
    cout << "الاسم: " << name << ", العمر: " << age << endl;

    // ======= 3. الطباعة مع عمليات حسابية مباشرة =======
    cout << "5 + 3 = " << (5 + 3) << endl;
    cout << "10 * 2 = " << (10 * 2) << endl;

    // ======= 4. استخدام fancier formatting =======
    double pi = 3.14159;
    cout << "قيمة PI تقريباً = " << pi << endl;

    // ======= 5. الإدخال من المستخدم (Input) =======
    int number;
    cout << "أدخل رقم: ";
    cin >> number; // قراءة رقم من المستخدم
    cout << "الرقم الذي أدخلته هو: " << number << endl;

    string text;
    cout << "أدخل اسمك: ";
    cin >> text; // قراءة كلمة واحدة (بدون مسافات)
    cout << "مرحباً، " << text << "!" << endl;

    // ======= 6. أفضل الممارسات =======
    // - استخدم endl أو '\n' للفصل بين الأسطر
    // - استخدم << لدمج النصوص والمتغيرات في cout
    // - عند القراءة استخدم cin بحذر مع النصوص التي تحتوي مسافات (قد تحتاج getline)
    // - ضع تعليقات لتوضيح ما يتم قراءته أو طباعته

    return 0;
}
